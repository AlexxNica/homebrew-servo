class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-08-29.tar.gz"
  version "2016.08.29"
  sha256 "8a013e977be27e040a91d5d4e1512e974df24fb749aed85ce097bd5ebb57dc6f"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
