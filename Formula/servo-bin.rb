class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-10-10.tar.gz"
  version "2016.10.10"
  sha256 "7a501d29ac9f25c59232a0a21cc9da249662b72ee8aaae6e4cbd8efccca8d8c3"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
