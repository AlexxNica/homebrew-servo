class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-02.tar.gz"
  version "2016.09.02"
  sha256 "042d24150b05bf57d00f2597699078fa8be336d8fc9aebf7b47310f6caff2bc2"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
