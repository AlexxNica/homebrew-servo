class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-10-06.tar.gz"
  version "2016.10.06"
  sha256 "00ce35326721e7000c1a5ef853809aeaffdfe33e7b7ae1ef092cd0c1242f9a5b"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
