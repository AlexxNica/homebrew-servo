class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-14.tar.gz"
  version "2016.09.14"
  sha256 "b37f6b1ad028ee85ea79be8fe6581eeda86168cb2dd5bda2020e068b9111e2e3"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
