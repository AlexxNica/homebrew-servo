class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-10-08.tar.gz"
  version "2016.10.08"
  sha256 "8c819f071c7c1367cfe29ad64b36a7def74111bea5ead5deff69801e70e8ef28"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
