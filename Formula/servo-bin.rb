class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-18.tar.gz"
  version "2016.09.18"
  sha256 "af6b016e5c66409676a31f1e04814688aac7a0385d6b8bd994a7e98664d8ec06"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
