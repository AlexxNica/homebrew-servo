class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-11-04.tar.gz"
  version "2016.11.04"
  sha256 "5801f69dc799669e666e71d01f6cc158cd0c041ea330e47e0e4914fa1dcc9807"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
