class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-10-24.tar.gz"
  version "2016.10.24"
  sha256 "ed7f927c07a0e42f2e9762fda3c7ff625d430a2dd2cd5c19ba945a99b4e09997"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
