class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-08-23.tar.gz"
  version "2016.08.23"
  sha256 "d2f76f3458e2bbbd3b153f5d2ece1dbed924f306b4365b227c12435fad3440f7"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
