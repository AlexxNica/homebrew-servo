class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-29.tar.gz"
  version "2016.09.29"
  sha256 "fad4e25a21d12142b4b7730cbbf9e3c3010f7c94b384443d5a6772a5712e1e99"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
