class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-10-26.tar.gz"
  version "2016.10.26"
  sha256 "cdb5b7f2f2a246190ac3e9ea21a83851927262cbe029cc117ac01d46d4cbc20a"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
