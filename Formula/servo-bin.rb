class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-11-01.tar.gz"
  version "2016.11.01"
  sha256 "6ddd1c60d8e0bc0bad82feccc562b3c1c2f91ad6be3760e71cc202b71ced75f6"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
