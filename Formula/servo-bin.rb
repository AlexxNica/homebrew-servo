class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-16.tar.gz"
  version "2016.09.16"
  sha256 "39c5e9b5998d58f96ee5e4a9f974f877919011208e14940b58d5250972fc5774"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
