class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-10-23.tar.gz"
  version "2016.10.23"
  sha256 "209cd88590a0ca61c11e6813a0d24d03e402500a0b2691d023e29f9dab98e7d7"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
