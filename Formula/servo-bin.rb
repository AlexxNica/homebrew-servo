class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-09-03.tar.gz"
  version "2016.09.03"
  sha256 "5f7dc331b4b3e67a27f7fdbb1e3303697c3c2eb419b9d5dba0f2e83a9df92b4e"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
