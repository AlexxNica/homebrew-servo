class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/servo-2016-08-31.tar.gz"
  version "2016.08.31"
  sha256 "d0c52ccf624ca233d96bfb9d7bbbfaed114622809cc1a23941ed30410d14cfd7"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
